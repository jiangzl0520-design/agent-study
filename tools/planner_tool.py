def build_plan_prompt(user_goal):
    """
    构建任务规划 Prompt。
    这个工具不直接调用大模型，而是负责生成一个结构化规划提示词。
    """

    prompt = f"""
用户的目标是：

{user_goal}

请你作为一个任务规划 Agent，帮用户制定一个清晰、可执行的计划。

要求：
1. 先分析用户目标
2. 把目标拆成多个阶段
3. 每个阶段给出具体任务
4. 每个任务尽量可执行
5. 适合初学者
6. 不要空泛，要具体
7. 最后给出今天立刻可以做的 3 个任务

请按下面格式输出：

## 目标分析

## 阶段拆解

### 第 1 阶段：
- 任务 1：
- 任务 2：
- 任务 3：

### 第 2 阶段：
- 任务 1：
- 任务 2：
- 任务 3：

## 今天立刻执行的 3 个任务

1.
2.
3.

## 注意事项
"""
    return prompt